package homeassignments.task3;

public class IncrementDecrement {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        x++;
        int c = x*y;
        y--;
        System.out.println("x = "+x);
        System.out.println("y = "+y);
        System.out.println("c = "+c);
    }
}
